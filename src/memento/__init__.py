def __getattr__(name: str):
    if name not in {"ChromaSemanticIndex", "Neo4jGraphStore"}:
        raise AttributeError(f"module {__name__!r} has no attribute {name!r}")

    module = import_module(".memory", __name__)
    value = getattr(module, name)
    globals()[name] = value
    return value
